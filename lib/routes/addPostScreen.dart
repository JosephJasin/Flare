                  thickness: 1,
                ),
                DropdownButton<String>(
                  isExpanded: true,
                  hint: Container(
                    alignment: Alignment.centerRight,
                    child: Text(
                      'الكلية',
                      textDirection: TextDirection.rtl,
                    ),
                  ),
                  value: faculty,
                  onChanged: (value) {
                    setState(() {
                      course = null;
                      faculty = value;
                    });
                  },
                  items: courses.keys.map((key) {
                    return DropdownMenuItem<String>(
                      child: Container(
                        alignment: Alignment.centerRight,
                        child: Text(
                          key,
                          textDirection: TextDirection.rtl,
                        ),
                      ),
                      value: key,
                      onTap: () {},
                    );
                  }).toList(),
                ),
                if (faculty != null)
                  DropdownButton<String>(
                    isExpanded: true,
                    hint: Container(
                      alignment: Alignment.centerRight,
                      child: Text(
                        'المواد',
                        textDirection: TextDirection.rtl,
                      ),
                    ),
                    value: course,
                    onChanged: (value) {
                      setState(() => course = value);
                    },
                    items: courses[faculty].map((value) {
                      return DropdownMenuItem<String>(
                        child: Container(
                          alignment: Alignment.centerRight,
                          child: Text(
                            value,
                            textDirection: TextDirection.rtl,
                          ),
                        ),
                        value: value,
                        onTap: () {},
                      );
